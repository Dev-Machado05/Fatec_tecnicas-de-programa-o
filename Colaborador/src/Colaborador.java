public class Colaborador {
    private double provento;
    private String nome;

    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public void setProvento(double provento) {
        this.provento = provento;
    }
    public double getProvento() {
        return provento;
    }
}
