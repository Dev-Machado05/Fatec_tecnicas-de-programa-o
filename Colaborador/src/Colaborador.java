public class Colaborador {
    private double provento;
    private String nome;

    public String getNome() { // aplicando o método get para nome
        return nome;
    }
    public void setNome(String nome) { // aplicando o método set para nome
        this.nome = nome;
    }
    public void setProvento(double provento) { // aplicando o método set para provento
        this.provento = provento;
    }
    public double getProvento() { // aplicando o método get para provento
        return provento;
    }
}
